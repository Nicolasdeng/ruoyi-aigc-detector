-- 1. 脑筋急转弯题库表
DROP TABLE IF EXISTS `riddle_question`;
CREATE TABLE `riddle_question` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '题目ID',
  `category` varchar(50) DEFAULT NULL COMMENT '分类（logic:逻辑推理/word:文字游戏/math:数学思维/life:生活常识/funny:搞笑幽默）',
  `difficulty` varchar(20) DEFAULT 'medium' COMMENT '难度（easy:简单/medium:中等/hard:困难）',
  `question` text NOT NULL COMMENT '题目内容',
  `answer` varchar(500) NOT NULL COMMENT '答案',
  `explanation` text COMMENT '答案解析',
  `views` int(11) DEFAULT 0 COMMENT '查看次数',
  `likes` int(11) DEFAULT 0 COMMENT '点赞数',
  `status` char(1) DEFAULT '0' COMMENT '状态（0正常 1停用）',
  `create_by` varchar(64) DEFAULT '' COMMENT '创建者',
  `create_time` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_by` varchar(64) DEFAULT '' COMMENT '更新者',
  `update_time` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `remark` varchar(500) DEFAULT NULL COMMENT '备注',
  PRIMARY KEY (`id`),
  KEY `idx_category` (`category`),
  KEY `idx_difficulty` (`difficulty`),
  KEY `idx_status` (`status`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4 COMMENT='脑筋急转弯题库表';

-- 2. 用户答题历史表
DROP TABLE IF EXISTS `riddle_history`;
CREATE TABLE `riddle_history` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '历史记录ID',
  `user_id` bigint(20) NOT NULL COMMENT '用户ID',
  `question_id` bigint(20) NOT NULL COMMENT '题目ID',
  `user_answer` varchar(500) DEFAULT NULL COMMENT '用户答案',
  `is_correct` char(1) DEFAULT '0' COMMENT '是否正确（0错误 1正确）',
  `answer_time` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '答题时间',
  `time_spent` int(11) DEFAULT 0 COMMENT '答题耗时（秒）',
  PRIMARY KEY (`id`),
  KEY `idx_user_id` (`user_id`),
  KEY `idx_question_id` (`question_id`),
  KEY `idx_answer_time` (`answer_time`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4 COMMENT='用户答题历史表';

-- 3. 用户收藏表
DROP TABLE IF EXISTS `riddle_favorite`;
CREATE TABLE `riddle_favorite` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '收藏ID',
  `user_id` bigint(20) NOT NULL COMMENT '用户ID',
  `question_id` bigint(20) NOT NULL COMMENT '题目ID',
  `create_time` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '收藏时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_user_question` (`user_id`, `question_id`),
  KEY `idx_user_id` (`user_id`),
  KEY `idx_question_id` (`question_id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4 COMMENT='题目收藏表';

